// Role distribution: [realKnowers, clueKnowers, spy=1]
export const ROLE_DISTRIBUTION = {
  3: { real: 1, clue: 1 },
  4: { real: 2, clue: 1 },
  5: { real: 2, clue: 2 },
  6: { real: 3, clue: 2 },
  7: { real: 4, clue: 2 },
  8: { real: 4, clue: 3 },
};

export function shuffleArray(arr) {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

export function pickRandomClues(clues, count = 3) {
  return shuffleArray(clues).slice(0, count);
}

export function generateRound(playerNames, locationIndex = null) {
  const count = playerNames.length;
  const dist = ROLE_DISTRIBUTION[count];
  if (!dist) return null;

  // Pick location
  const locIdx = locationIndex !== null ? locationIndex : Math.floor(Math.random() * LOCATIONS.length);
  const location = LOCATIONS[locIdx];

  // Build role array
  const roles = [];
  for (let i = 0; i < dist.real; i++) roles.push("real");
  for (let i = 0; i < dist.clue; i++) roles.push("clue");
  roles.push("spy");

  const shuffledRoles = shuffleArray(roles);
  const cluesForRound = pickRandomClues(location.clues, 3);

  const players = playerNames.map((name, i) => ({
    name,
    role: shuffledRoles[i],
    location: location.name,
    clues: cluesForRound,
  }));

  return { location: location.name, players };
}